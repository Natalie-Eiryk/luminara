/**
 * Ms. Luminara Quiz - Anatomy Diagram Helper v2.1
 * Serves local SVG/WebP diagrams with annotation overlays
 *
 * Features:
 * - Local diagram assets (fetched from Wikimedia Commons, stored in repo)
 * - Support for ZIM-extracted Wikipedia images
 * - SVG annotation overlays (arrows + region highlights)
 * - Question-to-diagram mapping via manifest
 * - Fallback to live Wikimedia search if local not found
 *
 * @module AnatomyDiagrams
 * @version 2.1.0
 */

const AnatomyDiagrams = {
  // Base path for diagram assets
  ASSETS_PATH: 'assets/diagrams',

  // Category folders (matches ZIM extraction output)
  CATEGORIES: ['612-physiology/612.82-brain', '612-physiology/612.81-nerves', '612-physiology/612.89-ans', '600-senses', '612-physiology/612.4-endocrine', 'general'],

  // Manifest data (loaded on init)
  manifest: null,
  manifestLoaded: false,

  // Index of ZIM-extracted images (built on init)
  zimIndex: {},
  zimIndexLoaded: false,

  // Cache for loaded diagrams
  cache: {},

  // Wikimedia Commons API (fallback)
  WIKI_API: 'https://commons.wikimedia.org/w/api.php',

  /**
   * Initialize - load the diagram manifest and scan for ZIM-extracted images
   */
  async init() {
    // Load manifest
    try {
      const resp = await fetch(`${this.ASSETS_PATH}/diagram-manifest.json`);
      if (resp.ok) {
        this.manifest = await resp.json();
        this.manifestLoaded = true;
        console.log(`[AnatomyDiagrams] Loaded manifest with ${Object.keys(this.manifest.diagrams).length} diagrams`);
      }
    } catch (e) {
      console.warn('[AnatomyDiagrams] Could not load manifest, will use live fallback:', e.message);
    }

    // Try to load ZIM index (generated by extract-diagrams.js)
    await this.loadZimIndex();
  },

  /**
   * Load index of ZIM-extracted images
   */
  async loadZimIndex() {
    try {
      const resp = await fetch(`${this.ASSETS_PATH}/zim-extracted-index.json`);
      if (resp.ok) {
        this.zimIndex = await resp.json();
        this.zimIndexLoaded = true;
        const count = Object.values(this.zimIndex).reduce((sum, cat) => sum + cat.length, 0);
        console.log(`[AnatomyDiagrams] Loaded ZIM index with ${count} extracted images`);
      }
      // Silently ignore 404 - zim-index.json is optional
    } catch (e) {
      // ZIM index doesn't exist yet - silently ignore
    }
  },

  /**
   * Search ZIM-extracted images by term
   * @param {string} term - Search term
   * @returns {object|null} Best matching image info
   */
  searchZimImages(term) {
    if (!this.zimIndexLoaded || !this.zimIndex) return null;

    const termLower = term.toLowerCase().replace(/\s+/g, '_');
    const termParts = termLower.split('_');
    let bestMatch = null;
    let bestScore = 0;

    for (const [category, files] of Object.entries(this.zimIndex)) {
      for (const file of files) {
        const fileLower = file.toLowerCase();
        let score = 0;

        // Exact match
        if (fileLower.includes(termLower)) {
          score = 100;
        } else {
          // Partial match - check each part of the term
          for (const part of termParts) {
            if (part.length > 2 && fileLower.includes(part)) {
              score += 20;
            }
          }
        }

        // Prefer SVG files
        if (file.endsWith('.svg')) score += 10;

        // Prefer Blausen (high quality medical illustrations)
        if (fileLower.includes('blausen')) score += 15;

        // Prefer Gray's Anatomy plates
        if (/gray[0-9]/.test(fileLower)) score += 10;

        if (score > bestScore) {
          bestScore = score;
          bestMatch = {
            file: `${category}/${file}`,
            category: category,
            filename: file,
            title: this.formatImageTitle(file),
            source: 'Wikipedia ZIM extraction',
            license: 'CC-BY-SA / Public Domain',
            isZimExtracted: true
          };
        }
      }
    }

    return bestScore >= 20 ? bestMatch : null;
  },

  /**
   * Format filename into readable title
   */
  formatImageTitle(filename) {
    return filename
      .replace(/\.(svg|png|jpg|jpeg|gif|webp)$/i, '')
      .replace(/_/g, ' ')
      .replace(/^(Blausen|Gray)[_\s]?[0-9]*[_\s]?/i, '')
      .replace(/\s+/g, ' ')
      .trim();
  },

  /**
   * Get diagram for a specific question
   * @param {object} question - Question object with id and text
   * @returns {object|null} Diagram info with annotations
   */
  getDiagramForQuestion(question) {
    if (!this.manifestLoaded || !this.manifest) return null;

    const qId = question.id;

    // 1. Check exact question mapping
    if (this.manifest.questionMappings[qId]) {
      const mapping = this.manifest.questionMappings[qId];
      const diagram = this.manifest.diagrams[mapping.diagram];
      if (diagram) {
        return {
          ...diagram,
          highlightRegions: mapping.highlight || [],
          questionId: qId
        };
      }
    }

    // 2. Check pattern mappings (e.g., "100.1.*" -> "brain-sagittal")
    const patterns = this.manifest.questionMappings._patterns || {};
    for (const [pattern, diagramId] of Object.entries(patterns)) {
      const regex = new RegExp('^' + pattern.replace(/\*/g, '.*') + '$');
      if (regex.test(qId)) {
        const diagram = this.manifest.diagrams[diagramId];
        if (diagram) {
          return {
            ...diagram,
            highlightRegions: [],
            questionId: qId
          };
        }
      }
    }

    // 3. Check term mappings based on question text
    const terms = this.extractTermsFromQuestion(question);
    for (const term of terms) {
      const termLower = term.toLowerCase();
      const diagramId = this.manifest.termMappings[termLower];
      if (diagramId && this.manifest.diagrams[diagramId]) {
        const diagram = this.manifest.diagrams[diagramId];
        // Find which region to highlight based on term
        const regionKey = this.findRegionForTerm(diagram, termLower);
        return {
          ...diagram,
          highlightRegions: regionKey ? [regionKey] : [],
          matchedTerm: term,
          questionId: qId
        };
      }
    }

    // 4. Check ZIM-extracted images
    if (this.zimIndexLoaded) {
      for (const term of terms) {
        const zimMatch = this.searchZimImages(term);
        if (zimMatch) {
          return {
            ...zimMatch,
            highlightRegions: [],
            matchedTerm: term,
            questionId: qId
          };
        }
      }
    }

    return null;
  },

  /**
   * Find which region in a diagram matches a term
   */
  findRegionForTerm(diagram, term) {
    if (!diagram.regions) return null;

    const termLower = term.toLowerCase().replace(/\s+/g, '-');

    // Exact match
    if (diagram.regions[termLower]) return termLower;

    // Partial match
    for (const regionKey of Object.keys(diagram.regions)) {
      if (regionKey.includes(termLower) || termLower.includes(regionKey.replace(/-/g, ' '))) {
        return regionKey;
      }
    }

    return null;
  },

  /**
   * Extract anatomy terms from a question
   */
  extractTermsFromQuestion(question) {
    if (!this.manifestLoaded || !this.manifest) return [];

    const text = (question.q || '') + ' ' + (question.options || []).join(' ');
    const textLower = text.toLowerCase();
    const terms = [];

    // Check all mapped terms
    for (const term of Object.keys(this.manifest.termMappings)) {
      if (textLower.includes(term)) {
        terms.push(term);
      }
    }

    return terms;
  },

  /**
   * Render diagram with annotation overlay
   * @param {object} diagramInfo - Diagram info from getDiagramForQuestion
   * @param {HTMLElement} container - Container element to render into
   */
  renderDiagram(diagramInfo, container) {
    if (!diagramInfo || !diagramInfo.file) {
      container.innerHTML = '<div class="diagram-empty">No diagram available</div>';
      return;
    }

    const imagePath = `${this.ASSETS_PATH}/${diagramInfo.file}`;
    const isSvg = diagramInfo.file.endsWith('.svg');
    const hasAnnotations = diagramInfo.regions && diagramInfo.highlightRegions && diagramInfo.highlightRegions.length > 0;

    // Build annotation overlay (only if we have region data)
    const overlayHTML = hasAnnotations ? this.buildAnnotationOverlay(diagramInfo) : '';

    // Source link - handle ZIM-extracted vs manifest images
    const sourceUrl = diagramInfo.source && diagramInfo.source.startsWith('http')
      ? diagramInfo.source
      : 'https://commons.wikimedia.org';
    const sourceLabel = diagramInfo.isZimExtracted
      ? 'Extracted from Wikipedia'
      : `${diagramInfo.license || 'CC'} - Wikimedia Commons`;

    container.innerHTML = `
      <div class="diagram-viewer">
        <div class="diagram-main" id="diagramMain">
          ${isSvg ? `
            <object type="image/svg+xml" data="${imagePath}" class="diagram-svg" id="diagramSvg">
              <img src="${imagePath}" alt="${diagramInfo.title}" class="diagram-img">
            </object>
          ` : `
            <img src="${imagePath}" alt="${diagramInfo.title}" class="diagram-img"
                 data-action="zoom" data-url="${imagePath}" style="cursor: zoom-in;">
          `}
          ${hasAnnotations ? `
            <svg class="diagram-overlay" id="diagramOverlay" viewBox="0 0 400 300">
              ${overlayHTML}
            </svg>
          ` : ''}
        </div>
        <div class="diagram-caption">
          <span class="diagram-title">${diagramInfo.title || 'Anatomy Diagram'}</span>
          <a href="${sourceUrl}" target="_blank" rel="noopener" class="diagram-source">
            ${sourceLabel}
          </a>
        </div>
        <div class="diagram-controls">
          ${hasAnnotations ? `
            <button class="diagram-ctrl-btn" data-action="toggle-annotations" title="Toggle annotations">
              <span id="annotToggleIcon">&#x1F3AF;</span>
            </button>
          ` : ''}
          <button class="diagram-ctrl-btn" data-action="zoom" data-url="${imagePath}" title="View full size">
            <span>&#x1F50D;</span>
          </button>
          ${sourceUrl.startsWith('http') ? `
            <button class="diagram-ctrl-btn" data-action="zoom" data-url="${sourceUrl}" title="View source">
              <span>&#x1F517;</span>
            </button>
          ` : ''}
        </div>
      </div>
    `;

    // Store diagram info on container for later reference
    container._diagramInfo = diagramInfo;

    // Attach event listeners for data-action elements
    this.attachDiagramEventListeners(container);

    // If SVG with annotations, inject highlights after it loads
    if (isSvg && hasAnnotations) {
      const svgObj = container.querySelector('#diagramSvg');
      if (svgObj) {
        svgObj.addEventListener('load', () => {
          this.injectSvgHighlights(svgObj, diagramInfo.highlightRegions, diagramInfo.regions);
        });
      }
    }
  },

  /**
   * Attach event listeners to diagram controls
   */
  attachDiagramEventListeners(container) {
    // Zoom buttons and images
    container.querySelectorAll('[data-action="zoom"]').forEach(el => {
      el.addEventListener('click', (e) => {
        const url = el.dataset.url;
        if (url) this.openFullSize(url);
      });
    });

    // Toggle annotations button
    const toggleBtn = container.querySelector('[data-action="toggle-annotations"]');
    if (toggleBtn) {
      toggleBtn.addEventListener('click', () => this.toggleAnnotations());
    }
  },

  /**
   * Build SVG annotation overlay (arrows + labels)
   */
  buildAnnotationOverlay(diagramInfo) {
    if (!diagramInfo.regions || !diagramInfo.highlightRegions) return '';

    let svg = '';
    const regions = diagramInfo.regions;
    const highlights = diagramInfo.highlightRegions;

    for (const regionKey of highlights) {
      const region = regions[regionKey];
      if (!region) continue;

      const { x, y, label, color } = region;
      const arrowColor = color || '#f59e0b';

      // Draw arrow pointing to region
      const arrowEndX = x;
      const arrowEndY = y;
      const arrowStartX = x - 50;
      const arrowStartY = y - 40;

      svg += `
        <g class="annotation-group" data-region="${regionKey}">
          <!-- Arrow line -->
          <line x1="${arrowStartX}" y1="${arrowStartY}" x2="${arrowEndX - 5}" y2="${arrowEndY - 5}"
                stroke="${arrowColor}" stroke-width="2" marker-end="url(#arrowhead)"/>

          <!-- Label background -->
          <rect x="${arrowStartX - 60}" y="${arrowStartY - 12}"
                width="${label.length * 7 + 10}" height="20"
                rx="4" fill="rgba(0,0,0,0.8)"/>

          <!-- Label text -->
          <text x="${arrowStartX - 55}" y="${arrowStartY + 3}"
                fill="${arrowColor}" font-size="11" font-weight="500">
            ${label}
          </text>

          <!-- Highlight circle -->
          <circle cx="${x}" cy="${y}" r="15"
                  fill="none" stroke="${arrowColor}" stroke-width="2"
                  stroke-dasharray="4 2" class="highlight-pulse"/>
        </g>
      `;
    }

    // Add arrowhead marker definition
    if (highlights.length > 0) {
      svg = `
        <defs>
          <marker id="arrowhead" markerWidth="10" markerHeight="7"
                  refX="9" refY="3.5" orient="auto">
            <polygon points="0 0, 10 3.5, 0 7" fill="#f59e0b"/>
          </marker>
        </defs>
      ` + svg;
    }

    return svg;
  },

  /**
   * Inject highlights directly into loaded SVG
   */
  injectSvgHighlights(svgObject, highlightRegions, regions) {
    try {
      const svgDoc = svgObject.contentDocument;
      if (!svgDoc) return;

      const svgRoot = svgDoc.querySelector('svg');
      if (!svgRoot) return;

      // Add highlight styles
      const style = svgDoc.createElementNS('http://www.w3.org/2000/svg', 'style');
      style.textContent = `
        .lumi-highlight {
          fill: rgba(245, 158, 11, 0.2);
          stroke: #f59e0b;
          stroke-width: 2;
          animation: pulse 1.5s ease-in-out infinite;
        }
        @keyframes pulse {
          0%, 100% { opacity: 0.7; }
          50% { opacity: 1; }
        }
      `;
      svgRoot.insertBefore(style, svgRoot.firstChild);

      // Try to highlight by finding elements with matching IDs or text
      for (const regionKey of highlightRegions) {
        const region = regions[regionKey];
        if (!region) continue;

        // Try to find element by ID (many Wikimedia SVGs have labeled elements)
        const idVariants = [
          regionKey,
          regionKey.replace(/-/g, '_'),
          regionKey.replace(/-/g, ''),
          region.label,
          region.label.replace(/\s+/g, '_')
        ];

        for (const id of idVariants) {
          const el = svgDoc.getElementById(id);
          if (el) {
            el.classList.add('lumi-highlight');
            break;
          }
        }
      }
    } catch (e) {
      // Cross-origin SVGs can't be modified - that's okay
      console.log('[AnatomyDiagrams] Could not inject SVG highlights (cross-origin)');
    }
  },

  /**
   * Toggle annotation visibility
   */
  toggleAnnotations() {
    const overlay = document.getElementById('diagramOverlay');
    const icon = document.getElementById('annotToggleIcon');

    if (overlay) {
      const isHidden = overlay.style.display === 'none';
      overlay.style.display = isHidden ? 'block' : 'none';
      if (icon) {
        icon.innerHTML = isHidden ? '&#x1F3AF;' : '&#x1F441;';
      }
    }
  },

  /**
   * Open full size image/source (with URL sanitization)
   */
  openFullSize(url) {
    // Sanitize URL to prevent javascript: injection
    if (!url || typeof url !== 'string') return;
    const trimmed = url.trim().toLowerCase();
    if (trimmed.startsWith('javascript:') ||
        trimmed.startsWith('data:text') ||
        trimmed.startsWith('vbscript:')) {
      console.warn('[AnatomyDiagrams] Blocked dangerous URL:', url.substring(0, 50));
      return;
    }
    window.open(url, '_blank', 'noopener,noreferrer');
  },

  /**
   * Show diagram modal for a question
   */
  async showDiagramForQuestion(question) {
    // Try local diagram first (manifest + ZIM-extracted)
    let diagramInfo = this.getDiagramForQuestion(question);

    // If no local match, try searching ZIM index directly with question text
    if (!diagramInfo && this.zimIndexLoaded) {
      const qText = question.q || '';
      // Extract key anatomy terms from question
      const anatomyTerms = qText.match(/\b(brain|neuron|nerve|eye|ear|spinal|cerebr|cortex|lobe|synapse|axon|dendrite|retina|cochlea|thyroid|adrenal|pituitary)\w*/gi);
      if (anatomyTerms) {
        for (const term of anatomyTerms) {
          const zimMatch = this.searchZimImages(term);
          if (zimMatch) {
            diagramInfo = zimMatch;
            break;
          }
        }
      }
    }

    if (!diagramInfo) {
      // Fallback to live Wikimedia search
      const terms = this.extractTermsFromQuestion(question);
      if (terms.length > 0) {
        const images = await this.searchWikimedia(terms[0]);
        if (images.length > 0) {
          diagramInfo = {
            title: images[0].title,
            file: null,
            source: images[0].pageUrl,
            imageUrl: images[0].thumbUrl,
            license: images[0].license,
            isRemote: true
          };
        }
      }
    }

    if (!diagramInfo) {
      this.showNoDiagramMessage(question.q);
      return;
    }

    this.showDiagramModal(diagramInfo);
  },

  /**
   * Show diagram in modal
   */
  showDiagramModal(diagramInfo) {
    // Remove existing modal
    const existing = document.querySelector('.diagram-modal');
    if (existing) existing.remove();

    const modal = document.createElement('div');
    modal.className = 'diagram-modal';
    modal.innerHTML = `
      <div class="diagram-modal-overlay" data-action="close-modal"></div>
      <div class="diagram-modal-content">
        <div class="diagram-modal-header">
          <h3>${diagramInfo.title || 'Anatomy Diagram'}</h3>
          <button class="diagram-modal-close" data-action="close-modal">&times;</button>
        </div>
        <div class="diagram-modal-body" id="diagramModalBody"></div>
        <div class="diagram-modal-footer">
          <p class="diagram-attribution">
            Image from <a href="https://commons.wikimedia.org" target="_blank">Wikimedia Commons</a>
            (${diagramInfo.license || 'CC licensed'})
          </p>
        </div>
      </div>
    `;

    document.body.appendChild(modal);
    requestAnimationFrame(() => modal.classList.add('visible'));

    // Attach close handlers
    modal.querySelectorAll('[data-action="close-modal"]').forEach(el => {
      el.addEventListener('click', () => modal.remove());
    });

    // Render diagram into modal body
    const body = document.getElementById('diagramModalBody');

    if (diagramInfo.isRemote) {
      // Remote image - just show the image
      body.innerHTML = `
        <div class="diagram-viewer">
          <div class="diagram-main">
            <img src="${diagramInfo.imageUrl}" alt="${diagramInfo.title}"
                 data-action="zoom" data-url="${diagramInfo.source}"
                 style="max-width: 100%; cursor: zoom-in;">
          </div>
          <div class="diagram-caption">
            <a href="${diagramInfo.source}" target="_blank" rel="noopener" class="diagram-source">
              View on Wikimedia Commons
            </a>
          </div>
        </div>
      `;
      // Attach event listener to remote image
      const img = body.querySelector('[data-action="zoom"]');
      if (img) {
        img.addEventListener('click', () => this.openFullSize(img.dataset.url));
      }
    } else {
      // Local diagram with annotations
      this.renderDiagram(diagramInfo, body);
    }
  },

  /**
   * Show no diagram message
   */
  showNoDiagramMessage(questionText) {
    const terms = questionText.substring(0, 50);
    const toast = document.createElement('div');
    toast.className = 'diagram-toast';
    toast.innerHTML = `
      <p>No diagram found for this question</p>
      <a href="https://commons.wikimedia.org/w/index.php?search=${encodeURIComponent(terms + ' anatomy')}&title=Special:MediaSearch&type=image"
         target="_blank" rel="noopener">
        Search Wikimedia Commons
      </a>
    `;

    document.body.appendChild(toast);
    requestAnimationFrame(() => toast.classList.add('visible'));

    setTimeout(() => {
      toast.classList.remove('visible');
      setTimeout(() => toast.remove(), 300);
    }, 4000);
  },

  /**
   * Fallback: Search Wikimedia Commons live
   */
  async searchWikimedia(query) {
    try {
      const params = new URLSearchParams({
        action: 'query',
        format: 'json',
        origin: '*',
        generator: 'search',
        gsrsearch: `${query} anatomy diagram`,
        gsrnamespace: '6',
        gsrlimit: '3',
        prop: 'imageinfo',
        iiprop: 'url|extmetadata',
        iiurlwidth: '400'
      });

      const resp = await fetch(`${this.WIKI_API}?${params}`);
      const data = await resp.json();

      if (!data.query || !data.query.pages) return [];

      return Object.values(data.query.pages).map(page => {
        const info = page.imageinfo?.[0] || {};
        const meta = info.extmetadata || {};
        return {
          title: page.title.replace('File:', ''),
          pageUrl: `https://commons.wikimedia.org/wiki/${encodeURIComponent(page.title)}`,
          thumbUrl: info.thumburl || info.url,
          license: meta.LicenseShortName?.value || 'CC'
        };
      });
    } catch (e) {
      console.error('[AnatomyDiagrams] Wikimedia search failed:', e);
      return [];
    }
  },

  /**
   * Create "Show Diagram" button for a question
   */
  createDiagramButton(question) {
    // Check if we have a diagram for this question
    const diagramInfo = this.getDiagramForQuestion(question);
    const terms = this.extractTermsFromQuestion(question);

    if (!diagramInfo && terms.length === 0) return null;

    const btn = document.createElement('button');
    btn.className = 'show-diagram-btn';
    btn.innerHTML = `<span class="btn-icon">&#x1F5BC;</span> Show Diagram`;

    if (diagramInfo) {
      btn.title = `View: ${diagramInfo.title}`;
      btn.classList.add('has-local');
    } else {
      btn.title = `Search for: ${terms.join(', ')}`;
    }

    btn.addEventListener('click', async () => {
      btn.disabled = true;
      btn.innerHTML = '<span class="btn-icon">&#x23F3;</span> Loading...';

      await this.showDiagramForQuestion(question);

      btn.disabled = false;
      btn.innerHTML = `<span class="btn-icon">&#x1F5BC;</span> Show Diagram`;
    });

    return btn;
  }
};

// Initialize on DOM ready
if (typeof document !== 'undefined') {
  document.addEventListener('DOMContentLoaded', () => {
    AnatomyDiagrams.init();
  });
}

// Global access
if (typeof window !== 'undefined') {
  window.AnatomyDiagrams = AnatomyDiagrams;
}
